
console.log("-----Data Types in JavaScript: -----");
console.log("JavaScript has five primitive data types: String, Number, Boolean, Undefined, and Null.");

console.log("In addition to primitive data types, JavaScript also has a non-primitive data type called Object.");
console.log("The non-primitive data types include Object, Array, and Function.");


console.log("---using String data type: ---");
//String data type
let name = "Meher Shyam";
console.log(name + " is a " + typeof name + " data type.");

let greeting = "Hello Manikonda Congrats on your new job";
console.log(greeting + " is a " + typeof greeting + " data type.");

console.log("---using Number data type: ---");
//Number data type
let age = 30;
console.log(age + " is a " + typeof age + " data type.");

let accountBalance = 1000.50;
console.log(accountBalance + " is a " + typeof accountBalance + " data type.");

console.log("---using Boolean data type: ---");
//Boolean data type
let isStudent = true;
console.log(isStudent + " is a " + typeof isStudent + " data type.");

let driverLicense = false;
console.log(driverLicense + " is a " + typeof driverLicense + " data type.");

console.log("---using Undefined data type: ---");
//Undefined data type
let undefinedValue;
console.log(undefinedValue + " is an " + typeof undefinedValue + " data type.");

let road;
console.log(road + " is an " + typeof road + " data type.");

console.log("---using Null data type: ---");
//Null data type
let nullValue = null;
console.log(nullValue + " is a " + typeof nullValue + " data type.");

let emptyValue = null;
console.log(emptyValue + " is a " + typeof emptyValue + " data type.");
