
//print numbers from 1 to 10 using do while loop
let i = 100;
do {
    console.log(i);
    i++;
} while (i <= 10);
console.log("Finished counting.");

//print even numbers from 2 to 20 using do while loop   
let j = 2;
do {
    console.log(j);
    j+=2;
} while (j <= 10);
console.log("Finished counting even numbers.");

//print numbers from 10 to 1 using do while loop
let k = 10;
do { 
    console.log(k);
    k--;
} while (k > 0);
console.log("Finished counting backwards.");